// ---- ตัวช่วย ----
function escapeRegExp(s: string) {
  return s.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}
// ทำ pattern ให้ยืดหยุ่น: เว้นวรรครอบ "/" เป็น optional (LLM เขียน "Net Debt/EBITDA" หรือ "Net Debt / EBITDA" ก็จับได้)
function toPattern(term: string) {
  return escapeRegExp(term).replace(/\s*\/\s*/g, "\\s*/\\s*").replace(/ +/g, "\\s+");
}
function norm(s: string) {
  return s.toLowerCase().replace(/\s*\/\s*/g, "/").replace(/\s+/g, " ").trim();
}

const TERMS = Object.keys(GLOSSARY).sort((a, b) => b.length - a.length); // ยาวก่อน กันชนกัน
const LOOKUP = new Map(Object.entries(GLOSSARY).map(([k, v]) => [norm(k), v]));
const RE = new RegExp("(?:" + TERMS.map(toPattern).join("|") + ")", "gi");

// span พร้อม tooltip (ใช้กับ badge/meta ได้ด้วย)
export function Tip({ def, children }: { def: string; children: React.ReactNode }) {
  if (!def) return <>{children}</>;
  return (
    <span className="term">
      {children}
      <span className="tip">{def}</span>
    </span>
  );
}

// รับข้อความ -> ไฮไลต์คำศัพท์ที่รู้จักพร้อม tooltip
export function GlossaryText({ text }: { text: string }) {
  const out: React.ReactNode[] = [];
  let last = 0;
  let m: RegExpExecArray | null;
  RE.lastIndex = 0;
  let i = 0;
  while ((m = RE.exec(text)) !== null) {
    const def = LOOKUP.get(norm(m[0]));
    if (m.index > last) out.push(text.slice(last, m.index));
    if (def) {
      out.push(
        <Tip key={i++} def={def}>
          {m[0]}
        </Tip>
      );
    } else {
      out.push(m[0]);
    }
    last = m.index + m[0].length;
    if (m[0].length === 0) RE.lastIndex++; // กันลูปไม่รู้จบ
  }
  if (last < text.length) out.push(text.slice(last));
  return <>{out}</>;
}
